<h2>Default Fields</h2>
<p>To set which details should be shown next to apartments by default go to Custom Fields-&gt;Default Fields and select the fields you would like shown in the top box. These can usually be overwritten for specific situations (widgets, shortcodes, etc.).</p>
<h2>Property Subtitle</h2>
<p>The fields shown in the property subtitle can be customized by going to Custom Fields-&gt;Default Fields and selecting the fields to show, and their order, in the second box on the screen. The  details selected will then be shown under the main title in various locations. </p>

