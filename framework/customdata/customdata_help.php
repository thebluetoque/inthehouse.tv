							<p>Estatement comes with an awesome custom field system. This system allows you to manage your properties extremely flexibly and with as little hassle as possible. Whenever you create a property you can add "details" to it. These details are split into two groups: global details and specific details</p>
<p>Global details are shown for each and every property you have, making data entry quick and painless. You can choose the type of data (dropdown, text field, checkboxes, etc. ), and then just add everything you need for each property.</p>
<p>Specific details can also be added on a property-to-property basis. These details are minor additions which are not applicable to all properties.</p>
